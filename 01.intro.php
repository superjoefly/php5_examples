    <h1>PHP Intro</h1>

    <p>PHP is a server side scripting language, and a powerful tool for making dynamic and interactive Web pages.</p>

    <p>PHP is a widely-used, free, and efficient alternative to competitors such as Microsoft's ASP.</p>

    <p>PHP scripts are executed on the server.</p>

    <p>PHP is an acronym for "PHP: Hypertext Preprocessor"</p>

    <hr />

    <pre><code class="language-php">
      &lt;?php<br />
      echo "My first PHP script!";<br />
      ?&gt;
    </code></pre>

    <hr />

    <h2>What is a PHP File?</h2>

    <p>PHP files contain text, HTML, CSS, JavaScript, and PHP code.</p>

    <p>PHP code is executed on the server, and the result is returned to the browser as plain HTML.</p>

    <p>PHP files have an extension of ".php".</p>

    <hr />

    <h2>What can PHP Do?</h2>

    <ul>
      <li>generate dynamic page content</li>
      <li>create, open, read, write, delete, and close files on the server</li>
      <li>collect form data</li>
      <li>send and receive cookies</li>
      <li>add, delete, modify data in you database</li>
      <li>control user-access</li>
      <li>encrypt data</li>
    </ul>

    <p>With PHP you are not limited to output HTML. You can also output images, PDF files, and even Flash moveis. You can also output any text, such as XHTML and XML.</p>

